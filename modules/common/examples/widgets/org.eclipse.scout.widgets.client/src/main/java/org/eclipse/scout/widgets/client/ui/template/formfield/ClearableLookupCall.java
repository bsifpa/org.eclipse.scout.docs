package org.eclipse.scout.widgets.client.ui.template.formfield;

import java.util.ArrayList;
import java.util.List;

import org.eclipse.scout.rt.client.ui.form.fields.IValueField;
import org.eclipse.scout.rt.platform.classid.ClassId;
import org.eclipse.scout.rt.shared.services.lookup.ILookupRow;
import org.eclipse.scout.rt.shared.services.lookup.LocalLookupCall;
import org.eclipse.scout.rt.shared.services.lookup.LookupRow;

@ClassId("87994332-f1fc-43bf-ac85-b6909b9da471")
public class ClearableLookupCall extends LocalLookupCall<String> {

  private static final long serialVersionUID = 1L;

  @Override
  protected List<? extends ILookupRow<String>> execCreateLookupRows() {
    List<ILookupRow<String>> rows = new ArrayList<>();
    rows.add(new LookupRow<>(IValueField.CLEARABLE_ALWAYS, "always"));
    rows.add(new LookupRow<>(IValueField.CLEARABLE_FOCUSED, "focused"));
    rows.add(new LookupRow<>(IValueField.CLEARABLE_NEVER, "never"));
    return rows;
  }
}
